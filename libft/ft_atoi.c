#include "libft.h"

int		ft_atoi(const char *s)
{
	size_t	i;
	int		res;
	int		sign;

	i = 0;
	res = 0;
	sign = 1;
	while (s[i] == '\t' || s[i] == '\v' || s[i] == '\f' ||
		s[i] == '\r' || s[i] == '\n' || s[i] == ' ')
		i++;
	if (s[i] == '-' || s[i] == '+')
		if (s[i++] == '-')
			sign = -1;
	while (s[i] >= '0' && s[i] <= '9')
		res = res * 10 + s[i++] - '0';
	return (res * sign);
}
