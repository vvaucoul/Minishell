#include "minishell.h"

int		b_env(t_mns *mns)
{
	int i;

	i = 0;
	while (mns->envp[i])
	{
		ft_putstr_fd(mns->envp[i], 1);
		ft_putchar_fd('\n', 1);
		++i;
	}
	return (0);
}
